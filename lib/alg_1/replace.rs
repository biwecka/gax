// Imports /////////////////////////////////////////////////////////////////////
use crate::population::Chromosome;

// Functions ///////////////////////////////////////////////////////////////////
pub fn elite_1(population: Vec<Chromosome>, children: Vec<Chromosome>) -> Vec<Chromosome> {
    let curr_best = population.first().cloned().unwrap();
    let mut new_pop = children;
    new_pop.push(curr_best);

    new_pop
}

